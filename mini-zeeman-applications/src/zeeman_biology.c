/* ==============================================================
 * Zeeman Heartbeat Model (1972, 1977)
 *
 * The heartbeat is modeled as a relaxation oscillation governed
 * by the cusp catastrophe. The muscle fiber length x (fast) and
 * electrochemical activity y (slow) interact through the cusp.
 *
 * Equations:
 *   epsilon * dx/dt = -(x^3 + a(x,y)*x + b(x,y))
 *   dy/dt = g(x, y)
 *
 * where a(x,y) = a0 + alpha*y, b(x,y) = b0 + beta*x
 * and g(x,y) drives the slow recovery.
 *
 * The cusp parameters oscillate, driving the system alternately
 * to the upper sheet (systole, contracted) and lower sheet
 * (diastole, relaxed).
 *
 * Reference: Zeeman (1972), "Differential equations for the
 * heartbeat and nerve impulse", in Towards a Theoretical Biology.
 * ============================================================== */

HeartbeatModel* heartbeat_create(double a0, double b0, double epsilon, double I) {
    HeartbeatModel* h = calloc(1, sizeof(HeartbeatModel));
    if (!h) return NULL;
    h->x = 0.5;         /* start at intermediate length */
    h->y = 0.0;
    h->a0 = a0;
    h->b0 = b0;
    h->epsilon = epsilon;
    h->I = I;           /* pacemaker stimulus */
    h->t = 0.0;
    return h;
}

void heartbeat_free(HeartbeatModel* h) {
    free(h);
}

/* ==============================================================
 * Heartbeat RHS
 *
 * The cusp parameters depend on the slow variable y:
 *   a = a0 + alpha*y (typically alpha > 0)
 *   b = b0 + beta*x + I (pacemaker drives b)
 *
 * Fast dynamics: epsilon * dx/dt = -(x^3 + a*x + b)
 * Slow dynamics: dy/dt = x - gamma*y (relaxation to equilibrium)
 *
 * During diastole: x is on the lower stable sheet
 * During systole:  x is on the upper stable sheet
 * The transition occurs when the current sheet vanishes
 * (delay convention) as a and b change.
 * ============================================================== */

void heartbeat_rhs(const HeartbeatModel* h, double* dx, double* dy) {
    /* Slow variable y modifies the normal factor a */
    double a = h->a0 - 3.0 * h->y;  /* coupling: y increases -> a decreases */
    /* Pacemaker stimulus I modifies the splitting factor b */
    double b = h->b0 + h->I;

    /* Fast equation: gradient descent on cusp potential */
    *dx = -(h->x * h->x * h->x + a * h->x + b) / h->epsilon;

    /* Slow recovery: y relaxes toward x with damping */
    *dy = h->x - 0.5 * h->y;

    /* Repolarization: after contraction, reset mechanism */
    if (h->x > 1.5) {
        *dy -= 2.0;  /* rapid repolarization */
    }
}

void heartbeat_step(HeartbeatModel* h, double dt) {
    double dx, dy;
    heartbeat_rhs(h, &dx, &dy);
    h->x += dx * dt;
    h->y += dy * dt;
    h->t += dt;

    /* Keep variables bounded */
    if (h->x > 3.0)  h->x = 3.0;
    if (h->x < -3.0) h->x = -3.0;
    if (h->y > 2.0)  h->y = 2.0;
    if (h->y < -2.0) h->y = -2.0;
}

void heartbeat_simulate(HeartbeatModel* h, double duration, double dt) {
    int steps = (int)(duration / dt);
    for (int i = 0; i < steps; i++) {
        heartbeat_step(h, dt);
    }
}

bool heartbeat_is_systole(const HeartbeatModel* h) {
    /* Systole: muscle fiber is contracted (x > threshold) */
    return h->x > 0.5;
}

double heartbeat_period(HeartbeatModel* h, double duration, double dt) {
    /* Detect period by counting systole-to-systole transitions */
    int steps = (int)(duration / dt);
    int beat_count = 0;
    double last_beat_time = 0.0;
    bool was_diastole = !heartbeat_is_systole(h);

    HeartbeatModel* h2 = heartbeat_create(h->a0, h->b0, h->epsilon, h->I);
    if (!h2) return 0.0;
    h2->x = h->x; h2->y = h->y; h2->t = h->t;

    for (int i = 0; i < steps; i++) {
        heartbeat_step(h2, dt);
        bool is_sys = heartbeat_is_systole(h2);
        if (is_sys && was_diastole) {
            double interval = h2->t - last_beat_time;
            if (beat_count > 0 && interval > 0.01) {
                /* Found at least one full period */
            }
            last_beat_time = h2->t;
            beat_count++;
        }
        was_diastole = !is_sys;
    }

    double period = (beat_count > 1) ? (h2->t / beat_count) : 0.0;
    heartbeat_free(h2);
    return period;
}

void heartbeat_find_fixed_points(const HeartbeatModel* h, CuspEquilibrium* eq_out) {
    /* Fixed points of the fast subsystem at current y */
    double a = h->a0 - 3.0 * h->y;
    double b = h->b0 + h->I;
    CuspEquilibrium* eq = cusp_equilibrium(a, b);
    if (eq && eq_out) {
        memcpy(eq_out, eq, sizeof(CuspEquilibrium));
    }
    cusp_equilibrium_free(eq);
}

void heartbeat_print(const HeartbeatModel* h) {
    printf("HeartbeatModel: x=%.4f y=%.4f a0=%.2f b0=%.2f eps=%.4f I=%.2f t=%.2f\n",
           h->x, h->y, h->a0, h->b0, h->epsilon, h->I, h->t);
    printf("  State: %s\n", heartbeat_is_systole(h) ? "SYSTOLE" : "DIASTOLE");
    double a = h->a0 - 3.0 * h->y;
    double b = h->b0 + h->I;
    printf("  Effective cusp: a=%.4f b=%.4f  bistable=%s\n",
           a, b, cusp_is_bistable(a, b) ? "YES" : "NO");
}

/* ==============================================================
 * Nerve Impulse Model (Zeeman 1973)
 *
 * The action potential is interpreted as a cusp catastrophe
 * in the nerve membrane dynamics.
 *
 * Fast variable v: membrane potential
 * Slow variable w: recovery (sodium inactivation, potassium activation)
 *
 * Equations (FitzHugh-Nagumo type):
 *   epsilon * dv/dt = -(v^3 + a*v + b(w, I))
 *   dw/dt = v - b_param * w
 *
 * The cusp structure explains:
 * - Threshold phenomenon: subthreshold stimuli die out,
 *   superthreshold stimuli trigger full action potential
 * - All-or-none law: action potential amplitude is fixed
 * - Refractory period: hysteresis prevents immediate re-firing
 * ============================================================== */

NerveImpulse* nerve_impulse_create(double a_param, double b_param, double I_ext) {
    NerveImpulse* n = calloc(1, sizeof(NerveImpulse));
    if (!n) return NULL;
    n->v = -1.0;        /* resting potential */
    n->w = -0.5;
    n->a_param = a_param;
    n->b_param = b_param;
    n->I_ext = I_ext;
    n->t = 0.0;
    return n;
}

void nerve_impulse_free(NerveImpulse* n) {
    free(n);
}

void nerve_impulse_rhs(const NerveImpulse* n, double* dv, double* dw) {
    /* Effective cusp parameters
     * a is constant (determines bistability)
     * b depends on w and external stimulus */
    double a = n->a_param;
    double b = n->w + n->I_ext;

    /* Fast voltage dynamics */
    double eps = 0.08;
    *dv = -(n->v * n->v * n->v + a * n->v + b) / eps;

    /* Slow recovery */
    *dw = n->v - n->b_param * n->w;
}

void nerve_impulse_step(NerveImpulse* n, double dt) {
    double dv, dw;
    nerve_impulse_rhs(n, &dv, &dw);
    n->v += dv * dt;
    n->w += dw * dt;
    n->t += dt;

    if (n->v > 2.0)  n->v = 2.0;
    if (n->v < -2.0) n->v = -2.0;
    if (n->w > 1.5)  n->w = 1.5;
    if (n->w < -1.5) n->w = -1.5;
}

void nerve_impulse_simulate(NerveImpulse* n, double duration, double dt) {
    int steps = (int)(duration / dt);
    for (int i = 0; i < steps; i++) {
        nerve_impulse_step(n, dt);
    }
}

double nerve_impulse_threshold(const NerveImpulse* n) {
    /* Threshold stimulus: the b-value where the lower stable
     * equilibrium vanishes (delay convention jump).
     * For a = -1, the bifurcation set is 4*(-1)^3 + 27*b^2 = 0
     * => -4 + 27*b^2 = 0 => b = +- 2/sqrt(27) ~ 0.385
     * The jump from resting occurs near b = -2/sqrt(27) ~ -0.385
     *
     * More generally, for given a < 0:
     * b_crit = sqrt(-4*a^3/27) */
    if (n->a_param >= 0.0) return 0.0;
    return -sqrt(-4.0 * n->a_param * n->a_param * n->a_param / 27.0);
}

bool nerve_impulse_is_firing(const NerveImpulse* n) {
    /* Firing when membrane potential is above threshold */
    return n->v > 0.0;
}

int nerve_impulse_spike_count(NerveImpulse* n, double duration, double dt) {
    int steps = (int)(duration / dt);
    int spikes = 0;
    bool was_low = true;

    NerveImpulse* n2 = nerve_impulse_create(n->a_param, n->b_param, n->I_ext);
    if (!n2) return 0;
    n2->v = n->v; n2->w = n->w; n2->t = n->t;

    for (int i = 0; i < steps; i++) {
        nerve_impulse_step(n2, dt);
        bool is_high = nerve_impulse_is_firing(n2);
        if (is_high && was_low) spikes++;
        was_low = !is_high;
    }
    nerve_impulse_free(n2);
    return spikes;
}

void nerve_impulse_print(const NerveImpulse* n) {
    printf("NerveImpulse: v=%.4f w=%.4f a=%.2f b=%.2f I=%.2f t=%.2f\n",
           n->v, n->w, n->a_param, n->b_param, n->I_ext, n->t);
    printf("  Firing: %s  Threshold: %.4f\n",
           nerve_impulse_is_firing(n) ? "YES" : "NO",
           nerve_impulse_threshold(n));
}

/* ==============================================================
 * Cell Differentiation Model (Thom 1975, Zeeman 1974)
 *
 * Cell differentiation is modeled as a cusp catastrophe where
 * the morphogen concentration serves as the splitting factor b.
 *
 * The cell state x represents position along a differentiation
 * axis (e.g., stem cell -> specialized cell types).
 *
 * Potential: V(x) = x^4/4 + a*x^2/2 + b(m)*x
 *
 * where b(m) = -threshold + m (morphogen drives differentiation)
 * and a < 0 gives bistability (two possible cell fates).
 *
 * As morphogen increases past the threshold, the system moves
 * from monostable (stem cell) through the cusp region to
 * a new stable state (differentiated cell).
 * ============================================================== */

CellDiffModel* cell_diff_create(double morphogen, double threshold,
                                 double hill_coef) {
    CellDiffModel* c = calloc(1, sizeof(CellDiffModel));
    if (!c) return NULL;
    c->x = 0.0;           /* undifferentiated state */
    c->y = 0.0;
    c->morphogen = morphogen;
    c->threshold = threshold;
    c->hill_coef = hill_coef;
    c->t = 0.0;
    return c;
}

void cell_diff_free(CellDiffModel* c) {
    free(c);
}

double cell_diff_potential(const CellDiffModel* c, double x) {
    /* Effective b depends on morphogen via Hill function */
    double b = -c->threshold
             + c->morphogen / (1.0 + c->morphogen); /* simplified Hill */
    double a = -2.0 * c->hill_coef; /* bistability when hill_coef > 0 */
    return cusp_potential(x, a, b);
}

void cell_diff_rhs(const CellDiffModel* c, double* dx, double* dy) {
    double b_eff = -c->threshold
                 + c->morphogen / (1.0 + c->morphogen);
    double a_eff = -2.0 * c->hill_coef;

    /* Gradient descent on potential */
    *dx = -(c->x * c->x * c->x + a_eff * c->x + b_eff);

    /* Gene regulatory dynamics */
    *dy = c->x - c->y + c->morphogen * 0.1;
}

void cell_diff_step(CellDiffModel* c, double dt) {
    double dx, dy;
    cell_diff_rhs(c, &dx, &dy);
    c->x += dx * dt;
    c->y += dy * dt;
    c->t += dt;
    if (c->x > 2.0)  c->x = 2.0;
    if (c->x < -2.0) c->x = -2.0;
}

void cell_diff_simulate(CellDiffModel* c, double duration, double dt) {
    int steps = (int)(duration / dt);
    for (int i = 0; i < steps; i++) {
        cell_diff_step(c, dt);
    }
}

double cell_diff_critical_morphogen(const CellDiffModel* c) {
    /* Critical morphogen level where the bifurcation occurs:
     * the system transitions from monostable to bistable when
     * the effective b crosses the cusp bifurcation set. */
    double a = -2.0 * c->hill_coef;
    if (a >= 0.0) return c->threshold; /* no bifurcation */

    /* On bifurcation set: 4*a^3 + 27*b^2 = 0
     * b_crit = sqrt(-4*a^3/27)
     * b_eff = -threshold + morphogen/(1+morphogen)
     * Set b_eff = b_crit, solve for morphogen: */
    double b_crit = sqrt(-4.0 * a * a * a / 27.0);
    double target_b = b_crit + c->threshold;
    /* morphogen/(1+morphogen) = target_b => morphogen = target_b/(1-target_b) */
    if (target_b >= 1.0 || target_b <= 0.0) return c->threshold;
    return target_b / (1.0 - target_b);
}

int cell_diff_stable_states(const CellDiffModel* c) {
    double b_eff = -c->threshold
                 + c->morphogen / (1.0 + c->morphogen);
    double a_eff = -2.0 * c->hill_coef;
    CuspEquilibrium* eq = cusp_equilibrium(a_eff, b_eff);
    int n = eq ? eq->n_stable : 0;
    cusp_equilibrium_free(eq);
    return n;
}

void cell_diff_print(const CellDiffModel* c) {
    printf("CellDiffModel: x=%.4f y=%.4f morphogen=%.2f threshold=%.2f h=%.2f t=%.2f\n",
           c->x, c->y, c->morphogen, c->threshold, c->hill_coef, c->t);
    int ns = cell_diff_stable_states(c);
    printf("  Stable states: %d  Critical morphogen: %.4f\n",
           ns, cell_diff_critical_morphogen(c));
    const char* fate = (c->x > 0.5) ? "DIFFERENTIATED"
                     : (c->x < -0.5) ? "ALTERNATIVE_FATE"
                     : "UNDIFFERENTIATED";
    printf("  Cell fate: %s\n", fate);
}
